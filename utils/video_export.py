def save_t2v_output(
    video_tensor: torch.Tensor,
    output_path: Union[str, Path],
    fps: int = 24,
    format: Optional[Literal["mp4", "gif", "frames"]] = None,
    **kwargs,
) -> Union[str, List[str]]:
    """
    High-level function to save T2V model output to various formats.

    Automatically detects format from file extension if not specified.

    Args:
        video_tensor: Video tensor with shape [B, C, T, H, W] or [C, T, H, W].
                      Expected value range: [-1, 1] (typical for diffusion models).
        output_path: Output path. Can be:
                     - A file path (e.g., "output.mp4", "output.gif")
                     - A directory path for frame export
        fps: Frames per second. Default: 24.
        format: Output format. If None, inferred from output_path extension.
        **kwargs: Additional arguments passed to the underlying export function.

    Returns:
        - For mp4/gif: str path to saved file
        - For frames: List[str] paths to saved frames

    Example:
        >>> video = model.generate("A cat playing")
        >>> save_t2v_output(video, "cat.mp4")  # -> "cat.mp4"
        >>> save_t2v_output(video, "cat.gif")  # -> "cat.gif"
        >>> save_t2v_output(video, "./frames/")  # -> ["./frames/frame_00.png", ...]
    """
    output_path = Path(output_path)

    # Infer format from path
    if format is None:
        suffix = output_path.suffix.lower()
        if suffix == ".mp4":
            format = "mp4"
        elif suffix == ".gif":
            format = "gif"
        elif suffix == "" or output_path.is_dir():
            format = "frames"
        else:
            raise ValueError(
                f"Cannot infer format from path '{output_path}'. "
                "Specify format explicitly or use .mp4/.gif extension."
            )

    # Export based on format
    if format == "mp4":
        return tensor_to_video(video_tensor, output_path, fps=fps, **kwargs)
    elif format == "gif":
        return tensor_to_gif(video_tensor, output_path, fps=fps, **kwargs)
    elif format == "frames":
        return tensor_to_frames(video_tensor, output_path, **kwargs)
    else:
        raise ValueError(f"Unknown format: {format}")
