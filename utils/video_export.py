"""
Video export utilities for text-to-video (T2V) model outputs.

This module provides functions to convert video tensors generated by T2V models
into standard video file formats (MP4, GIF) or image sequences.

Typical usage:
    from utils.video_export import tensor_to_video

    # After T2V inference
    video_frames, _ = inference_t2v(model, tokenizer, text_encoder, vae, prompt, device)

    # Export to video file
    tensor_to_video(video_frames, "output.mp4", fps=24)
"""

import tempfile
from pathlib import Path
from typing import List, Literal, Optional, Union

import numpy as np
import torch

try:
    from PIL import Image
except ImportError:
    Image = None


def tensor_to_video(
    video_tensor: torch.Tensor,
    output_path: Union[str, Path],
    fps: int = 24,
    quality: float = 8.0,
    bitrate: Optional[int] = None,
    value_range: Literal["auto", "[-1,1]", "[0,1]"] = "auto",
    batch_index: int = 0,
) -> str:
    """
    Convert a video tensor from T2V model to a video file.

    Args:
        video_tensor: Video tensor with shape [B, C, T, H, W] or [C, T, H, W].
                      C should be 3 (RGB channels).
        output_path: Output video file path (should end with .mp4).
        fps: Frames per second for output video. Default: 24.
        quality: Video quality (0-10, higher is better). Default: 8.0.
                 Uses variable bitrate encoding.
        bitrate: Optional constant bitrate in bits/second. If set, overrides quality.
        value_range: Expected value range of input tensor.
                     - "auto": Auto-detect based on min/max values
                     - "[-1,1]": Values in range [-1, 1] (typical for diffusion models)
                     - "[0,1]": Values in range [0, 1]
        batch_index: Which batch sample to export if batch_size > 1. Default: 0.

    Returns:
        str: Path to the saved video file.

    Raises:
        ImportError: If imageio or imageio-ffmpeg is not installed.
        ValueError: If tensor shape is invalid.

    Example:
        >>> video_tensor = model.generate(prompt)  # [1, 3, 21, 480, 720]
        >>> tensor_to_video(video_tensor, "output.mp4", fps=24)
    """
    try:
        import imageio
    except ImportError:
        raise ImportError(
            "imageio is required for video export. "
            "Install with: pip install imageio imageio-ffmpeg"
        )

    # Validate and reshape tensor
    video_tensor = _prepare_tensor(video_tensor, batch_index)

    # Convert value range to [0, 255] uint8
    frames = _tensor_to_frames(video_tensor, value_range)

    # Export using imageio
    output_path = str(output_path)
    with imageio.get_writer(
        output_path,
        fps=fps,
        quality=quality,
        bitrate=bitrate,
        macro_block_size=16,
    ) as writer:
        for frame in frames:
            writer.append_data(frame)

    return output_path


def tensor_to_gif(
    video_tensor: torch.Tensor,
    output_path: Union[str, Path],
    fps: int = 10,
    value_range: Literal["auto", "[-1,1]", "[0,1]"] = "auto",
    batch_index: int = 0,
    loop: int = 0,
    optimize: bool = True,
) -> str:
    """
    Convert a video tensor from T2V model to an animated GIF.

    Args:
        video_tensor: Video tensor with shape [B, C, T, H, W] or [C, T, H, W].
        output_path: Output GIF file path.
        fps: Frames per second. Default: 10 (GIFs typically use lower fps).
        value_range: Expected value range of input tensor.
        batch_index: Which batch sample to export if batch_size > 1.
        loop: Number of times to loop (0 = infinite). Default: 0.
        optimize: Whether to optimize GIF size. Default: True.

    Returns:
        str: Path to the saved GIF file.

    Example:
        >>> video_tensor = model.generate(prompt)
        >>> tensor_to_gif(video_tensor, "output.gif", fps=10)
    """
    if Image is None:
        raise ImportError(
            "Pillow is required for GIF export. Install with: pip install Pillow"
        )

    # Validate and reshape tensor
    video_tensor = _prepare_tensor(video_tensor, batch_index)

    # Convert to PIL images
    pil_frames = _tensor_to_pil_frames(video_tensor, value_range)

    # Calculate frame duration in milliseconds
    duration = int(1000 / fps)

    # Save as GIF
    output_path = str(output_path)
    pil_frames[0].save(
        output_path,
        save_all=True,
        append_images=pil_frames[1:],
        duration=duration,
        loop=loop,
        optimize=optimize,
    )

    return output_path


def tensor_to_frames(
    video_tensor: torch.Tensor,
    output_dir: Union[str, Path],
    format: Literal["png", "jpg", "jpeg"] = "png",
    prefix: str = "frame",
    value_range: Literal["auto", "[-1,1]", "[0,1]"] = "auto",
    batch_index: int = 0,
    quality: int = 95,
) -> List[str]:
    """
    Save video tensor as individual image frames.

    Args:
        video_tensor: Video tensor with shape [B, C, T, H, W] or [C, T, H, W].
        output_dir: Directory to save frames.
        format: Image format ("png", "jpg", or "jpeg"). Default: "png".
        prefix: Filename prefix. Default: "frame".
        value_range: Expected value range of input tensor.
        batch_index: Which batch sample to export if batch_size > 1.
        quality: JPEG quality (1-100). Only used for jpg/jpeg format.

    Returns:
        List[str]: Paths to all saved frame files.

    Example:
        >>> video_tensor = model.generate(prompt)
        >>> frame_paths = tensor_to_frames(video_tensor, "./frames/")
    """
    if Image is None:
        raise ImportError(
            "Pillow is required for image export. Install with: pip install Pillow"
        )

    # Validate and reshape tensor
    video_tensor = _prepare_tensor(video_tensor, batch_index)

    # Convert to PIL images
    pil_frames = _tensor_to_pil_frames(video_tensor, value_range)

    # Create output directory
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    # Save frames
    saved_paths = []
    num_digits = len(str(len(pil_frames)))

    for i, frame in enumerate(pil_frames):
        filename = f"{prefix}_{i:0{num_digits}d}.{format}"
        filepath = output_dir / filename

        save_kwargs = {}
        if format.lower() in ("jpg", "jpeg"):
            save_kwargs["quality"] = quality

        frame.save(filepath, **save_kwargs)
        saved_paths.append(str(filepath))

    return saved_paths


def tensor_to_numpy_frames(
    video_tensor: torch.Tensor,
    value_range: Literal["auto", "[-1,1]", "[0,1]"] = "auto",
    batch_index: int = 0,
    output_dtype: np.dtype = np.uint8,
) -> np.ndarray:
    """
    Convert video tensor to numpy array of frames.

    This is useful for custom processing or integration with other libraries.

    Args:
        video_tensor: Video tensor with shape [B, C, T, H, W] or [C, T, H, W].
        value_range: Expected value range of input tensor.
        batch_index: Which batch sample to export if batch_size > 1.
        output_dtype: Output numpy dtype. Default: np.uint8 (values 0-255).
                      Use np.float32 for normalized [0, 1] output.

    Returns:
        np.ndarray: Array of frames with shape [T, H, W, C].
                    - If output_dtype is uint8: values in [0, 255]
                    - If output_dtype is float32/float64: values in [0, 1]

    Example:
        >>> video_tensor = model.generate(prompt)
        >>> frames = tensor_to_numpy_frames(video_tensor)
        >>> print(frames.shape)  # (21, 480, 720, 3)
    """
    # Validate and reshape tensor
    video_tensor = _prepare_tensor(video_tensor, batch_index)

    # Normalize to [0, 1]
    frames = _normalize_tensor(video_tensor, value_range)

    # Rearrange from [C, T, H, W] to [T, H, W, C]
    frames = frames.permute(1, 2, 3, 0).cpu().numpy()

    if np.issubdtype(output_dtype, np.integer):
        # Convert to integer range
        max_val = np.iinfo(output_dtype).max
        frames = (frames * max_val).clip(0, max_val).astype(output_dtype)
    else:
        frames = frames.astype(output_dtype)

    return frames


