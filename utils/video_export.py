def _prepare_tensor(
    video_tensor: torch.Tensor,
    batch_index: int = 0,
) -> torch.Tensor:
    """
    Validate and prepare video tensor.

    Args:
        video_tensor: Input tensor with shape [B, C, T, H, W] or [C, T, H, W].
        batch_index: Which batch sample to use.

    Returns:
        torch.Tensor: Tensor with shape [C, T, H, W].
    """
    # Handle different input dimensions
    if video_tensor.dim() == 5:
        # [B, C, T, H, W] -> [C, T, H, W]
        if batch_index >= video_tensor.shape[0]:
            raise ValueError(
                f"batch_index {batch_index} out of range for tensor with "
                f"batch_size {video_tensor.shape[0]}"
            )
        video_tensor = video_tensor[batch_index]
    elif video_tensor.dim() == 4:
        # [C, T, H, W] - already correct shape
        pass
    else:
        raise ValueError(
            f"Expected 4D or 5D tensor, got {video_tensor.dim()}D tensor "
            f"with shape {video_tensor.shape}"
        )

    # Validate channel dimension
    if video_tensor.shape[0] != 3:
        raise ValueError(
            f"Expected 3 channels (RGB), got {video_tensor.shape[0]} channels. "
            f"Tensor shape: {video_tensor.shape}"
        )

    return video_tensor


def _normalize_tensor(
    video_tensor: torch.Tensor,
    value_range: Literal["auto", "[-1,1]", "[0,1]"],
) -> torch.Tensor:
    """
    Normalize tensor values to [0, 1] range.

    Args:
        video_tensor: Input tensor with shape [C, T, H, W].
        value_range: Expected value range.

    Returns:
        torch.Tensor: Normalized tensor with values in [0, 1].
    """
    if value_range == "auto":
        min_val = video_tensor.min().item()
        max_val = video_tensor.max().item()

        # Heuristic: if min < -0.5, assume [-1, 1] range
        if min_val < -0.5:
            value_range = "[-1,1]"
        else:
            value_range = "[0,1]"

    if value_range == "[-1,1]":
        # Convert from [-1, 1] to [0, 1]
        video_tensor = (video_tensor + 1) / 2
    # else: already in [0, 1]

    # Clamp to valid range
    video_tensor = video_tensor.clamp(0, 1)

    return video_tensor


def _tensor_to_frames(
    video_tensor: torch.Tensor,
    value_range: Literal["auto", "[-1,1]", "[0,1]"],
) -> List[np.ndarray]:
    """
    Convert video tensor to list of numpy frames.

    Args:
        video_tensor: Input tensor with shape [C, T, H, W].
        value_range: Expected value range.

    Returns:
        List[np.ndarray]: List of frames, each with shape [H, W, C] and dtype uint8.
    """
    # Normalize to [0, 1]
    video_tensor = _normalize_tensor(video_tensor, value_range)

    # Convert to uint8
    video_tensor = (video_tensor * 255).clamp(0, 255).to(torch.uint8)

    # Rearrange from [C, T, H, W] to list of [H, W, C]
    video_tensor = video_tensor.permute(1, 2, 3, 0)  # [T, H, W, C]
    video_tensor = video_tensor.cpu().numpy()

    frames = [video_tensor[i] for i in range(video_tensor.shape[0])]
    return frames


def _tensor_to_pil_frames(
    video_tensor: torch.Tensor,
    value_range: Literal["auto", "[-1,1]", "[0,1]"],
) -> List["Image.Image"]:
    """
    Convert video tensor to list of PIL images.

    Args:
        video_tensor: Input tensor with shape [C, T, H, W].
        value_range: Expected value range.

    Returns:
        List[PIL.Image.Image]: List of PIL images.
    """
    frames = _tensor_to_frames(video_tensor, value_range)
    return [Image.fromarray(frame) for frame in frames]


